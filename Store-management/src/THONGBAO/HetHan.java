    public void ViewTable() {
        LocalDate c = java.time.LocalDate.now();
        Date date = Date.from(c.atStartOfDay(ZoneId.systemDefault()).toInstant());
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-dd-MM");
        String formatDate = formatter.format(date);
        this.txtTG.setText(formatDate);
        DefaultTableModel model = (DefaultTableModel) tb.getModel();
        String sql = "SELECT * FROM doan.san_pham where hansudung<'"+ c+"'";
        DOAN.Controller.getInstance().open();
        ResultSet rs = DOAN.Controller.getInstance().excuteQuery(sql);
        int tongTien = 0;
        try {
            int i = 0;
            int j = 0;
            while (rs.next()) {
                model.setValueAt(rs.getInt(1), j, 0);
                model.setValueAt(rs.getString(2), j, 1);
                model.setValueAt(rs.getInt(4), j, 3);
                model.setValueAt(rs.getInt(6), j, 4);
                model.setValueAt(rs.getString(3), j, 2);
                i++;
                j++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
